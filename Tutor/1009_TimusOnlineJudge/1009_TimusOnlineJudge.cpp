typedef long long ll;

int N,K;

ll ways[20][20];

ll getWays(int i, int lastChoice)
{

	if (i==1)
		return K-1;

	if (ways[i][lastChoice]!=-1)
		return ways[i][lastChoice];

	ll total=0;
	if (lastChoice==0)
	{

		for (int d=1;d<K;d++)
		{
			total+=getWays(i-1,d);
		}

	}
	else
	{
		for (int d=0;d<K;d++)
		{
			total+=getWays(i-1,d);
		}
	}

	return ways[i][lastChoice]=total;
}


int main()
{

	for (int i=0;i<20;i++)
		for (int j=0;j<20;j++)
			ways[i][j]=-1;

	cin>>N>>K;
	
	print(getWays(N,2));
	return 0;
}