int n;
vI ways(10000,-1);
vector<int> path[10000];

int getWays(int slide)
{
	if (slide==n)
	{
		return 1;
	}

	if (ways[slide]!=-1)
	{
		return ways[slide];
	}

	int total=0;

	for (int nextSlide: path[slide])
	{
		total+=getWays(nextSlide);
	}
	
	return ways[slide]=total;
}


